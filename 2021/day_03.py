from functools import reduce


def bits2int(bits):
    return int(''.join(str(i) for i in bits), base=2)


def get_bits_stats(arr, idx):
    bits_count = len(arr)
    y_bits = reduce(lambda acc, row: acc + row[idx], arr, 0)
    return y_bits, bits_count - y_bits


def calc_rate(arr, cmp_fn):
    row_len = len(arr[0])
    bits = [
        1 if cmp_fn(*get_bits_stats(arr, i)) else 0
        for i in range(row_len)
    ]
    return bits2int(bits)


def calc_power_consumption(arr):
    gamma = calc_rate(arr, lambda x, y: x > y)
    epsilon = calc_rate(arr, lambda x, y: x < y)
    return gamma, epsilon, gamma * epsilon


def most_common_value(y_bits, n_bits):
    if y_bits > n_bits:
        return 1
    elif n_bits > y_bits:
        return 0
    else:
        return 1


def least_common_value(y_bits, n_bits):
    if y_bits > n_bits:
        return 0
    elif n_bits > y_bits:
        return 1
    else:
        return 0


def filter_bits_array(arr, bit_criteria):
    row_len = len(arr[0])
    check_arr = arr
    for i in range(row_len):
        if len(check_arr) == 1:
            return bits2int(check_arr[0])

        mcv = bit_criteria(*get_bits_stats(check_arr, i))

        check_arr = [bits for bits in check_arr if bits[i] == mcv]

    return bits2int(check_arr[0])


def calc_oxygen_generator_rating(arr):
    return filter_bits_array(arr, most_common_value)


def calc_c02_scrubber_rating(arr):
    return filter_bits_array(arr, least_common_value)


