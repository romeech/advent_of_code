def emulate_latterfish_growth(fish_set, days):
    prev_fishes = fish_set
    for day in range(days):
        new_fishes = [0] * len(prev_fishes)
        for i, fish in enumerate(prev_fishes):
            if fish == 0:
                new_fishes[i] = 6
                new_fishes.append(8)
            else:
                new_fishes[i] = fish - 1
        prev_fishes = new_fishes
    return len(prev_fishes)


